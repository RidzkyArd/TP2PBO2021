    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        String merk = t_merk.getText();
        String plat = t_plat.getText();
        String warna = t_warna.getText();
        String jenis = cb_jenis.getSelectedItem().toString(); 
        String query = "INSERT INTO data" + "(merk, plat, warna, jenis) VALUES" + " (?, ?, ?, ?)";
        if(merk.equals("") || plat.equals("") || warna.equals("") || jenis.equals("")){
            JOptionPane.showMessageDialog(null, "Masukan data secara lengkap!");
        } 
        else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_mobil", "root", ""); 
                PreparedStatement stmt = koneksi.prepareStatement(query);
                stmt.setString(1, merk);
                stmt.setString(2, plat);
                stmt.setString(3, warna);
                stmt.setString(4, jenis);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dimasukan!");   
            } 
            catch (Exception e){
               JOptionPane.showMessageDialog(null, "error");
            }
            t_merk.setText("");
            t_plat.setText("");
            t_warna.setText("");
            cb_jenis.setSelectedItem("");
        }
    }//GEN-LAST:event_btn_submitActionPerformed

