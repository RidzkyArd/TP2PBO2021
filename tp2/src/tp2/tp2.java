        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.getDataVector( ).removeAllElements( );
        model.fireTableDataChanged( );

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_mobil", "root", "");
            Statement st;
            st = koneksi.createStatement();
            String sql = "select * from data";
            ResultSet result = st.executeQuery(sql);

            while(result.next ()) {
                 Object[ ] obj = new Object[6];
                 obj[0] = result.getString("no");
                 obj[1] = result.getString("merk");
                 obj[2] = result.getString("plat");
                 obj[3] = result.getString("warna");
                 obj[4] = result.getString("jenis");

                 model.addRow(obj);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "error");
        }
